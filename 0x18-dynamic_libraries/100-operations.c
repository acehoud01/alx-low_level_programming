#include <stdio.h>

/**
 * add - function that adds two numbers
 * @a: an integer to add
 * @b: an integer to add
 *
 * Return: a + b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function that subtract one number
 * @a: an integer to sub
 * @b: an int to sub
 *
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function that mutiply two numbers
 * @a: first product
 * @b: second product
 *
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function tha divide two numbers
 * @a: diviser
 * @b: divisee
 *
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function that check a remainder
 * @a: first number
 * @b: second number
 *
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
